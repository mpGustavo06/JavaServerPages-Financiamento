package modelo;

public class Prestacao {

    float valorParcelaMensal;
    float valorJurosMensal;
    float totalPagarMensal;

    public float getValorParcelaMensal() {
        return valorParcelaMensal;
    }

    public void setValorParcelaMensal(float valorParcelaMensal) {
        this.valorParcelaMensal = valorParcelaMensal;
    }

    public float getValorJurosMensal() {
        return valorJurosMensal;
    }

    public void setValorJurosMensal(float valorJurosMensal) {
        this.valorJurosMensal = valorJurosMensal;
    }

    public float getTotalPagarMensal() {
        return totalPagarMensal;
    }

    public void setTotalPagarMensal(float totalPagarMensal) {
        this.totalPagarMensal = totalPagarMensal;
    }
}
